package businessinte.strategies;

public class TuplaMesEjercicioEntradas{
	
	private Integer entradas, ejercicio;
	private Long idMes;
	
	public Integer getEntradas() {
		return entradas;
	}
	public void setEntradas(Integer entradas_) {
		this.entradas = entradas_;
	}
	public Integer getEjercicio() {
		return ejercicio;
	}
	public void setEjercicio(Integer ejercicio_) {
		this.ejercicio = ejercicio_;
	}
	public Long getIdMes() {
		return idMes;
	}
	public void setIdMes(Long idMes_) {
		this.idMes = idMes_;
	}	
	
}
